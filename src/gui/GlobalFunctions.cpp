CString  G_ReadChinese(CString Str)
{
	CString Res = Str;

	// Chinese
	int count = Res.Find(_T("Chinese*"), 0);
	if (count != -1) //chinese中文
	{
		Str.Replace(_T("Chinese"), _T(""));

		CString Name = "";

		int Len = Str.GetLength();
		int NewLen = Len;
		CStringA StrId = "";

		for (int k = 0; k < Len; k++)
		{
			CString Namestr = Str.Left(1);

			if (Namestr == "*")
			{
				BOOL Chinese = FALSE;

				TCHAR ch = StrId.GetAt(0);
				int Len = StrId.GetLength();

				if (Len > 1)
				{
					Chinese = TRUE;
					StrId.Replace("C", "");
				}

				if (Chinese)
				{
					CString StrIds(StrId);

					if (InputIsIntNum(StrIds))
					{
						int id = atoi(StrId);
						Name += G_GetChinese(id);
					}
					else
					{
						Name += StrId;
					}
				}
				else
				{
					Name += StrId;
				}
				StrId = "";
			}
			else
			{
				StrId += Namestr;
			}

			Str = Str.Right(NewLen - 1);
			NewLen = Str.GetLength();
		}

		CString SS(StrId);
		Str = Name + SS;
	}

	return Str;
}

