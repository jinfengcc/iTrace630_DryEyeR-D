CString  G_ConvertChineseStr(CString Str)
{
	CString Res = Str;

	BOOL Chinese = FALSE;

	int len = Str.GetLength();

	for (int k = 0; k < len; k++)
	{
		TCHAR ch = Str.GetAt(k);

		if (ch > 255)
		{
			Chinese = TRUE;
			break;
		}
	}

	if (Chinese) //chinese中文
	{
		CStringA AName, CName;
		AName = "Chinese";
		CName = "";

		CString s;

		int NewLen = len;

		for (int k = 0; k < len; k++)
		{
			CString Namestr = Str.Left(1);

			TCHAR ch = Namestr.GetAt(0);

			if (ch > 255)
			{
				int ChineseID = G_GetChineseId(Namestr);
				if (ChineseID == -1)
				{
					CName += Namestr;
					CName += "*";
				}
				else
				{
					s.Format(_T("C%i*"), ChineseID);
					CName += s;
				}
			}
			else
			{
				CName += Namestr;
				CName += "*";
			}

			Str = Str.Right(NewLen - 1);
			NewLen = Str.GetLength();
		}

		Res = AName + "*" + CName;
	}

	return Res;
}

