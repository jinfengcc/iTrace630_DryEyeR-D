void LimbusAdjust(CWFExam* pWFExam, CCTExam* pCTExam)
{
	CEyeImage* pWFImage = &pWFExam->m_Image;
	CEyeImage* pCTImage = &pCTExam->m_Image;

	if (pWFImage->m_li_r_mean_um != pCTImage->m_li_r_mean_um
		|| pWFImage->m_li_r_min_um != pCTImage->m_li_r_min_um
		|| pWFImage->m_li_r_max_um != pCTImage->m_li_r_max_um)
	{
		pWFImage->m_li_r_mean_um = pCTImage->m_li_r_mean_um;
		pWFImage->m_li_r_min_um = pCTImage->m_li_r_min_um;
		pWFImage->m_li_r_max_um = pCTImage->m_li_r_max_um;

		for (int a = 0; a < 360; a++)
		{
			pWFImage->m_li_r_um[a] = pCTImage->m_li_r_um[a];
		}

		double xdis = pCTImage->m_li_x0_um - pCTImage->m_ve_x_um;
		double ydis = pCTImage->m_li_y0_um - pCTImage->m_ve_y_um;

		pWFImage->m_li_x0_um = pWFImage->m_ve_x_um + xdis;
		pWFImage->m_li_y0_um = pWFImage->m_ve_y_um + ydis;

		CString s = pWFExam->m_Header.m_AutoNote;

		int count = s.Find(_T("\nAngle Kappa"), 0);

		pWFExam->m_Header.m_AutoNote = s.Left(count);

		pWFExam->CalcAngles();

		s = pWFExam->m_Header.m_AutoNote;

		::DB.SaveExam(pWFExam);
	}

	pWFImage->m_li_ok = pCTImage->m_li_ok;
}

